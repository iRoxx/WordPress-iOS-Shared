import UIKit

class WPStyleGuide: NSObject {
    func nuxFormText() -> UIColor {
        return UIColor(red: CGFloat(50/255), green: CGFloat(65/255), blue: CGFloat(85/255), alpha: CGFloat(1.0))
    }

    func nuxFormPlaceholderText() -> UIColor {
        return UIColor(red: CGFloat(144/255), green: CGFloat(174/255), blue: CGFloat(194/255), alpha: CGFloat(1.0))
    }
}
